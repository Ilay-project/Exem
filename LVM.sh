#what is LVM and what it purpose


#LVM (Logical Volumes) meaning that we can creat/resize/delete LVM partitions from the command line while the linux system is running.
#No need to reboot the system to make the karnel aware of the newly created or resized partitions/

#The purposes of using LVM

# If we have more then one hard disk, LVM (Logical Volumes) can extend over more then one disk, they are not limited by the size of one single disk.
# Further, we can create a snapshot of any LV you can revert the original LV to the snapshot at a later time or delete the snapshot if we no longer need it.


